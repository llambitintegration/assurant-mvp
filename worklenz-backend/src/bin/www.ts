#!/usr/bin/env node

// config should be imported at the top of this file.
import "./config";

import {Server, Socket} from "socket.io";
import http, {IncomingHttpHeaders} from "http";

import app from "../app";
import {register} from "../socket.io";
import {IO} from "../shared/io";
import sessionMiddleware from "../middlewares/session-middleware";
import {getLoggedInUserIdFromSocket} from "../socket.io/util";
import {startCronJobs} from "../cron_jobs";
import FileConstants from "../shared/file-constants";
import DbTaskStatusChangeListener from "../pg_notify_listeners/db-task-status-changed";
import {isDocker, isReplit} from "../shared/utils";

function normalizePort(val?: string) {
  const p = parseInt(val || "0", 10);
  if (isNaN(p)) return val; // named pipe
  if (p >= 0) return p; // port number
  return false;
}

const port = normalizePort(process.env.PORT);
app.set("port", port);

const server = http.createServer(app);

/**
 * Build Socket.io CORS origins based on environment
 * Supports Docker, Replit, and local development
 */
const getSocketCorsOrigins = (): string[] | string => {
  const corsEnv = process.env.SOCKET_IO_CORS;
  
  // If wildcard is set, allow all origins
  if (corsEnv === '*') {
    return '*';
  }
  
  // Start with configured origins (if any)
  const origins: string[] = corsEnv ? corsEnv.split(',').map(o => o.trim()) : [];
  
  // Add Docker-specific origins
  if (isDocker()) {
    origins.push(
      "http://frontend:5000",
      "http://localhost:5000",
      "http://localhost:3000"
    );
  }
  
  // Add Replit-detected origins
  if (isReplit()) {
    const replitDomains = process.env.REPLIT_DOMAINS || process.env.REPLIT_DEV_DOMAIN;
    if (replitDomains) {
      replitDomains.split(',').forEach(domain => {
        const cleanDomain = domain.trim().replace(/\/+$/, '');
        if (cleanDomain) {
          origins.push(`https://${cleanDomain}`);
          origins.push(`http://${cleanDomain}`);
        }
      });
    }
  }
  
  // Always include localhost for development
  origins.push(
    "http://localhost:5000",
    "http://localhost:3000",
    "http://127.0.0.1:5000"
  );
  
  // Return unique origins or wildcard if empty
  const uniqueOrigins = [...new Set(origins.filter(Boolean))];
  return uniqueOrigins.length > 0 ? uniqueOrigins : '*';
};

const socketCorsOrigins = getSocketCorsOrigins();
console.log('[Socket.io] CORS origins:', Array.isArray(socketCorsOrigins) ? socketCorsOrigins.slice(0, 5) : socketCorsOrigins);

const io = new Server(server, {
  transports: ["polling", "websocket"],
  path: "/socket",
  cors: {
    origin: socketCorsOrigins,
    credentials: true
  },
  cookie: true,
  allowUpgrades: true
});

const wrap = (middleware: any) => (socket: any, next: any) => middleware(socket.request, {}, next);

io.use(wrap(sessionMiddleware));

io.use((socket, next) => {
  const userId = getLoggedInUserIdFromSocket(socket);
  if (userId)
    return next();
  return next(new Error("401 unauthorized"));
});

io.engine.on("initial_headers", (headers: IncomingHttpHeaders) => {
  headers["Strict-Transport-Security"] = "max-age=63072000; includeSubDomains";
  headers["X-Content-Type-Options"] = "nosniff";
  headers["X-Frame-Options"] = "Deny";
  headers["X-XSS-Protection"] = "1; mode=block";
});

io.on("connection", (socket: Socket) => {
  register(io, socket);
});

IO.setInstance(io);

function onError(error: any) {
  DbTaskStatusChangeListener.disconnect();

  if (error.syscall !== "listen") {
    throw error;
  }

  const bind = typeof port === "string"
    ? `Pipe ${port}`
    : `Port ${port}`;

  // handle specific listen errors with friendly messages
  switch (error.code) {
    case "EACCES":
      console.error(`${bind} requires elevated privileges`);
      process.exit(1);
      break;
    case "EADDRINUSE":
      console.error(`${bind} is already in use`);
      process.exit(1);
      break;
    default:
      throw error;
  }
}

function onListening() {
  const addr = server.address();
  if (!addr) return;

  const bind = typeof addr === "string"
    ? `pipe ${addr}`
    : `port ${addr.port}`;

  process.env.ENABLE_EMAIL_CRONJOBS === "true" && startCronJobs();
  // void initRedis();
  FileConstants.init();
  void DbTaskStatusChangeListener.connect();

  console.info(`Listening on ${bind}`);
}

function onClose() {
  DbTaskStatusChangeListener.disconnect();
}

server.on("error", onError);
server.on("close", onClose);
server.on("listening", onListening);

process.on("SIGINT", () => {
  server.close();
});

server.listen(port);
